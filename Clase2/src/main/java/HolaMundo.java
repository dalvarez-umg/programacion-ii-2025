// Clase principal del programa
public class HolaMundo {

    // Método principal: punto de entrada del programa
    // public: Significa que el método puede ser llamado desde fuera de la clase, en este caso por la máquina virtual de Java (JVM) al ejecutar el programa.
    // static: Porque el método main se ejecuta sin necesidad de crear un objeto de la clase.
    // void: Significa que no devuelve ningún valor.
    // String[] args: Permite que el programa reciba argumentos desde la línea de comandos
    public static void main(String[] args) {
        // Muestra el mensaje en la consola
        System.out.println("¡Hola Mundo!");
    }
}